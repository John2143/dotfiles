@activity.defn(name="tool_show_frame")
async def tool_show_frame_activity(arg: dict) -> dict:
    """Load, resize, and display a frame/crop/transcode source image to the model."""
    msg_path = arg["msg_path"]
    tool_args = arg.get("args", {})
    state, agent_dir = _load_state(msg_path)
    outcome_messages = []
    frames_dir = str(Path(agent_dir).parent)

    tc_id = _find_tc_id(state, "show_frame")
    source = tool_args.get("source", "snapshot://")

    # Parse resolution suffix: @low, @high, @max
    resolution = None
    base_source = source
    if "@" in source:
        base_source, res_str = source.rsplit("@", 1)
        if res_str in ("low", "high", "max"):
            resolution = res_str

    # Parse source type and frame spec
    if base_source.startswith("frame://"):
        frame_spec = base_source[len("frame://"):]
        src_type = "frame"
    elif base_source.startswith("transcode://"):
        frame_spec = base_source[len("transcode://"):]
        src_type = "transcode"
    elif base_source.startswith("crop://"):
        frame_spec = base_source[len("crop://"):]
        src_type = "crop"
    elif base_source.startswith("upscale://"):
        frame_spec = base_source[len("upscale://"):]
        src_type = "upscale"
    else:
        src_type = "snapshot"
        frame_spec = ""

    if src_type == "snapshot":
        snap_data = _s3_get(f"{frames_dir}/snapshot.jpg")
        if snap_data is not None:
            img = Image.open(_io.BytesIO(snap_data))
            fname = f"display_{uuid4().hex[:12]}.jpg"
            buf = _io.BytesIO()
            img.save(buf, "JPEG", quality=85)
            _s3_put(f"{agent_dir}/{fname}", buf.getvalue())
            ref = f"[[{fname}]]"
            outcome_messages.append({
                "role": "user",
                "content": [
                    {"type": "image_url", "image_url": {"url": ref}},
                    {"type": "text", "text": f"Detection snapshot ({img.width}x{img.height})."},
                ],
            })
        else:
            if tc_id:
                outcome_messages.append({
                    "role": "tool", "tool_call_id": tc_id,
                    "content": "No snapshot available.",
                })
    else:
        # Determine if range (N-M) or single (N)
        frames_list = []
        if "-" in frame_spec:
            try:
                f_start, f_end = int(frame_spec.split("-")[0]), int(frame_spec.split("-")[1])
                frames_list = list(range(f_start, f_end + 1))
            except (ValueError, IndexError):
                frames_list = []
        elif frame_spec:
            try:
                frames_list = [int(frame_spec)]
            except ValueError:
                frames_list = []

        # For transcode source, frame_spec is "batch/frame_idx" or "batch/start-end"
        batch = 0
        if src_type == "transcode":
            parts = frame_spec.split("/")
            if len(parts) >= 2:
                batch = int(parts[0])
                frame_part = parts[1]
                if "-" in frame_part:
                    rp = frame_part.split("-")
                    frames_list = list(range(int(rp[0]), int(rp[1]) + 1))
                else:
                    frames_list = [int(frame_part)]
            else:
                frames_list = []

        img_content = []
        if not frames_list:
            if tc_id:
                outcome_messages.append({
                    "role": "tool", "tool_call_id": tc_id,
                    "content": f"Invalid frame source: {source}",
                })
            return {"frames_shown": 0, "resolution": resolution, "error": "invalid_source", "messages": outcome_messages}
        elif len(frames_list) > 30:
            if tc_id:
                outcome_messages.append({
                    "role": "tool", "tool_call_id": tc_id,
                    "content": f"Too many frames ({len(frames_list)}). Show at most 30 per call. Scan in batches.",
                })
            return {"frames_shown": 0, "resolution": resolution, "error": "too_many_frames", "messages": outcome_messages}
        else:
            # Compute default resolution from frame count
            if resolution is None:
                n_frames = len(frames_list)
                if n_frames == 1:
                    resolution = "max"
                elif n_frames <= 9:
                    resolution = "high"
                elif n_frames <= 19:
                    resolution = "med"
                else:
                    resolution = "tiny"
            img_content = []
            img_native_w = img_native_h = 0

            for fi in frames_list:
                if src_type == "frame":
                    img_data = _s3_get(f"{frames_dir}/frames/frame_{fi:03d}.jpg")
                elif src_type == "transcode":
                    img_data = _s3_get(f"{agent_dir}/transcode_{batch:03d}_{fi:03d}.jpg")
                elif src_type == "crop":
                    img_data = _s3_get(f"{agent_dir}/crop_{fi:03d}.jpg")
                elif src_type == "upscale":
                    img_data = _s3_get(f"{agent_dir}/upscale_{fi:03d}.jpg")
                else:
                    img_data = _s3_get(f"{frames_dir}/snapshot.jpg")
                if img_data is None:
                    if src_type == "transcode":
                        msg = (
                            f"Transcode batch {batch} not found. "
                            f"Call transcode(start={batch}) first to generate this batch."
                        )
                    else:
                        msg = f"Source not found: {source}"
                    if tc_id:
                        outcome_messages.append({
                            "role": "tool", "tool_call_id": tc_id, "content": msg,
                        })
                    continue
                try:
                    img = Image.open(_io.BytesIO(img_data))
                    native_w, native_h = img.size
                    if not img_native_w:
                        img_native_w, img_native_h = native_w, native_h
                    tw, th = _resolution_pixels(resolution, img.size)
                    img = _resize_to(img, tw)
                except Exception as e:
                    log.exception("Failed to load frame from source %s", source)
                    continue
                fname = f"display_{uuid4().hex[:12]}.jpg"
                buf = _io.BytesIO()
                img.save(buf, "JPEG", quality=85)
                _s3_put(f"{agent_dir}/{fname}", buf.getvalue())
                ref = f"[[{fname}]]"
                img_content.append({"type": "image_url", "image_url": {"url": ref}})
            if img_content:
                label_text = f"{len(frames_list)} frame(s) at {resolution} resolution (native {img_native_w}x{img_native_h})."
                if src_type == "transcode":
                    label_text += f" From transcode batch {batch}."
                img_content.append({"type": "text", "text": label_text})
                outcome_messages.append({"role": "user", "content": img_content})

    frames_shown = len(img_content) - 1 if img_content else 0  # subtract label text

    return {"frames_shown": frames_shown, "resolution": resolution, "messages": outcome_messages}

@activity.defn(name="tool_transcode")
def tool_transcode_activity(arg: dict) -> dict:
    """Extract frames via ffmpeg (CPU-heavy, sync in thread pool), save to agent_dir."""

    msg_path = arg["msg_path"]
    tool_args = arg.get("args", {})
    event_id = arg.get("event_id", "")

    state, agent_dir = _load_state(msg_path)
    outcome_messages = []

    tc_id = _find_tc_id(state, "transcode")
    batch_start = tool_args.get("start", 0)
    max_frames = arg.get("max_frames", state.get("max_frames", 0))
    clip_duration = (arg.get("end_time", state.get("end_time", 0))
                     - arg.get("start_time", state.get("start_time", 0)))

    # Map frame index to time offset within the clip
    start_offset = clip_duration * (batch_start / max(1, max_frames))
    duration = max(1.0, min(10.0, float(tool_args.get("duration", 1.0))))

    frames = _transcode_frames(
        arg.get("camera", state.get("camera", "")),
        arg.get("start_time", state.get("start_time", 0)),
        arg.get("end_time", state.get("end_time", 0)),
        start_offset, duration, fps=None,
    )
    for i, f in enumerate(frames):
        _s3_put(f'{agent_dir}/transcode_{batch_start:03d}_{i:03d}.jpg', f)

    n_frames = len(frames)
    fps_effective = n_frames / duration if duration > 0 else 0
    end_offset = start_offset + duration
    if tc_id:
        outcome_messages.append({
            "role": "tool", "tool_call_id": tc_id,
            "content": (
                f"Transcoded {n_frames} HD frames at {fps_effective:.1f} fps "
                f"from {start_offset:.2f}s to {end_offset:.2f}s of the clip "
                f"(frame {batch_start}, {duration:.1f}s window). "
                f"Available: transcode://{batch_start}/0 through "
                f"transcode://{batch_start}/{n_frames - 1}. "
                f"You have NOT viewed these frames yet. Call show_frame() to inspect them. "
                f"Scan a range at @low first (e.g. show_frame('transcode://{batch_start}/0-5@low')) "
                f"— many low-res views cost less than one HD view. Then zoom in on specific frames "
                f"at @high or @max, and crop() for detail. "
                f"Do NOT call set_description() until you have inspected at least some frames."
            ),
        })
    log.info("tool_transcode: event=%s frames=%d [%d] dur=%.1fs fps=%.1f",
             event_id, n_frames, batch_start, duration, fps_effective)
    return {"frames_extracted": n_frames, "batch_start": batch_start,
            "batch_end": batch_start + n_frames - 1,
            "fps": round(fps_effective, 1),
            "start_offset": round(start_offset, 2),
            "end_offset": round(end_offset, 2),
            "duration_secs": duration,
            "messages": outcome_messages}


@activity.defn(name="tool_crop")
async def tool_crop_activity(arg: dict) -> dict:
    """Crop a region from a source image and save the result to agent_dir."""

    msg_path = arg["msg_path"]
    tool_args = arg.get("args", {})
    state, agent_dir = _load_state(msg_path)
    event_prefix = agent_dir.rsplit("/", 1)[0]

    tc_id = _find_tc_id(state, "crop")
    source = tool_args.get("source", "")
    outcome_messages = []

    x1 = max(0.0, min(1.0, float(tool_args.get("x1", 0))))
    y1 = max(0.0, min(1.0, float(tool_args.get("y1", 0))))
    x2 = max(0.0, min(1.0, float(tool_args.get("x2", 1))))
    y2 = max(0.0, min(1.0, float(tool_args.get("y2", 1))))

    if x1 >= x2 or y1 >= y2:
        if tc_id:
            outcome_messages.append({
                "role": "tool", "tool_call_id": tc_id,
                "content": f"Invalid crop region ({x1},{y1})-({x2},{y2})",
            })
    else:
        base_source = source
        if "@" in source:
            base_source = source.rsplit("@", 1)[0]

        # snapshot:// rejected early
        if base_source.startswith("snapshot://"):
            if tc_id:
                outcome_messages.append({
                    "role": "tool", "tool_call_id": tc_id,
                    "content": (
                        "Cannot crop snapshot://. The snapshot is a low-res bounding box "
                        "preview. Use frame://N to find the object in the recording frames."
                    ),
                })
            return {"messages": outcome_messages, "crop_ids": [], "source": source, "error": "snapshot_rejected"}


        # Resolve source list: each entry is (src_type, *args)
        source_list = []
        if base_source.startswith("frame://"):
            spec = base_source[len("frame://"):]
            if "-" in spec:
                rp = spec.split("-")
                source_list = [("frame", i) for i in range(int(rp[0]), int(rp[1]) + 1)]
            else:
                source_list = [("frame", int(spec))]
        elif base_source.startswith("transcode://"):
            parts = base_source[len("transcode://"):].split("/")
            batch = int(parts[0])
            frame_part = parts[1] if len(parts) > 1 else "0"
            if "-" in frame_part:
                rp = frame_part.split("-")
                source_list = [("transcode", batch, i) for i in range(int(rp[0]), int(rp[1]) + 1)]
            else:
                source_list = [("transcode", batch, int(frame_part))]
        elif base_source.startswith("crop://"):
            idx = int(base_source[len("crop://"):])
            source_list = [("crop", idx)]
        elif base_source.startswith("upscale://"):
            idx = int(base_source[len("upscale://"):])
            source_list = [("upscale", idx)]

        # Crop each source, collect results
        crop_results = []
        for entry in source_list:
            img_key = None
            if entry[0] == "frame":
                img_key = f"{event_prefix}/frames/frame_{entry[1]:03d}.jpg"
            elif entry[0] == "transcode":
                img_key = f"{agent_dir}/transcode_{entry[1]:03d}_{entry[2]:03d}.jpg"
            elif entry[0] == "crop":
                img_key = f"{agent_dir}/crop_{entry[1]:03d}.jpg"
            elif entry[0] == "upscale":
                img_key = f"{agent_dir}/upscale_{entry[1]:03d}.jpg"

            if not img_key:
                continue
            img_bytes = _s3_get(img_key)
            if img_bytes is None:
                continue
            try:
                img = Image.open(_io.BytesIO(img_bytes))
                w, h = img.size
                crop_box = (int(x1 * w), int(y1 * h), int(x2 * w), int(y2 * h))
                cropped = img.crop(crop_box)
                existing = _s3_list(f"{agent_dir}/crop_")
                crop_id = len(existing) + 1
                fname = f"crop_{crop_id:03d}.jpg"
                buf = _io.BytesIO()
                cropped.save(buf, "JPEG", quality=95)
                _s3_put(f"{agent_dir}/{fname}", buf.getvalue())
                crop_results.append((crop_id, fname, cropped.width, cropped.height))
            except Exception as e:
                log.exception("Failed to crop source %r", entry)

        if crop_results:
            crop_ids = [cid for cid, _, _, _ in crop_results]
            content_parts = []
            for _, fname, _, _ in crop_results:
                content_parts.append({"type": "image_url", "image_url": {"url": f"[[{fname}]]"}})
            if len(crop_results) == 1:
                _, _, cw, ch = crop_results[0]
                label = (
                    f"Cropped ({x1:.2f},{y1:.2f})-({x2:.2f},{y2:.2f}) "
                    f"→ {cw}x{ch} from {base_source}. Stored as crop://{crop_ids[0]}."
                )
            else:
                label = (
                    f"Cropped ({x1:.2f},{y1:.2f})-({x2:.2f},{y2:.2f}) "
                    f"from {base_source} ({len(crop_results)} frames). "
                    f"Stored as crop://{crop_ids[0]} through crop://{crop_ids[-1]}."
                )
            content_parts.append({"type": "text", "text": label})
            outcome_messages.append({"role": "user", "content": content_parts})
        else:
            if tc_id:
                outcome_messages.append({
                    "role": "tool", "tool_call_id": tc_id,
                    "content": f"No sources could be cropped from: {source}",
                })

    return {"messages": outcome_messages,
            "crop_ids": [cid for cid, _, _, _ in crop_results] if crop_results else [],
            "source": source, "crop_region": [x1, y1, x2, y2], "count": len(crop_results)}


@activity.defn(name="tool_compact")
async def tool_compact_activity(arg: dict) -> dict:
    """Compact context: distill findings via LLM, strip old images, preserve same-turn crops."""

    msg_path = arg["msg_path"]
    provider_path = arg.get("provider_path", "/var/lib/frigate-genai-sidecar/provider.json")
    model = arg.get("model", "gemini/gemini-2.5-flash")
    state, agent_dir = _load_state(msg_path)
    outcome_messages = []


    tc_id = _find_tc_id(state, "compact")

    # Find the assistant message that called compact — only strip BEFORE it
    compact_assistant_idx = None
    for i in range(len(state["messages"]) - 1, -1, -1):
        tcs = state["messages"][i].get("tool_calls", [])
        names = [t.get("function", {}).get("name", "") for t in tcs]
        if "compact" in names:
            compact_assistant_idx = i
            break

    # Extract text from messages up to the compact-calling assistant
    conv_lines = []
    for m in state["messages"][:compact_assistant_idx]:
        role = m.get("role", "")
        content = m.get("content", "")
        if isinstance(content, list):
            text = " ".join(p.get("text", "") for p in content if isinstance(p, dict) and p.get("type") == "text")
        else:
            text = str(content) if content else ""
        if text.strip():
            prefix = "User" if role == "user" else ("Assistant" if role == "assistant" else role)
            conv_lines.append(f"[{prefix}] {text.strip()}")
    # Also include cropped/upscaled content from same turn (messages after assistant)
    for m in state["messages"][compact_assistant_idx + 1:] if compact_assistant_idx is not None else []:
        role = m.get("role", "")
        content = m.get("content", "")
        if isinstance(content, list):
            text = " ".join(p.get("text", "") for p in content if isinstance(p, dict) and p.get("type") == "text")
        elif isinstance(content, str):
            text = content
        else:
            text = ""
        if text.strip() and role == "user":
            conv_lines.append(f"[Tool result] {text.strip()}")

    all_keys = _s3_list(agent_dir + "/")
    crop_files = sorted(k for k in all_keys if k.rsplit("/", 1)[-1].startswith("crop_"))
    upscale_files = sorted(k for k in all_keys if k.rsplit("/", 1)[-1].startswith("upscale_"))

    conv_text = "\n".join(conv_lines) if conv_lines else "(no prior conversation)"
    crop_list = ", ".join(f"crop://{int(cf.rsplit('/')[-1].replace('crop_','').rsplit('.',1)[0])}" for cf in crop_files[:20])
    upscale_list = ", ".join(f"upscale://{int(uf.rsplit('/')[-1].replace('upscale_','').rsplit('.',1)[0])}" for uf in upscale_files[:10])

    summary_prompt = (
        "You are summarizing a visual investigation. From the conversation below, produce a single "
        "paragraph covering:\n"
        "- Which frames were useful (with indices + resolution) and what they showed.\n"
        "- Which frames were NOT useful (empty, occluded, too dark).\n"
        "- Where to look next if investigation should continue.\n"
        "- What to re-view to refresh your memory (specific frame://N or crop://N).\n\n"
        f"Available crops: {crop_list or 'none'}\n"
        f"Available upscales: {upscale_list or 'none'}\n\n"
        "Conversation:\n"
        f"{conv_text}\n\n"
        "Compact summary:"
    )

    # Call LLM for distilled summary
    try:
        provider_cfg = load_json(provider_path)
        client, model_name = _resolve_provider(provider_cfg, model)
        resp = await _run_with_heartbeat(
            lambda: client.chat.completions.create(
                model=model_name,
                messages=[{"role": "user", "content": summary_prompt}],
                temperature=0.1,
                max_tokens=300,
            ),
            interval=5.0,
        )
        summary_text = resp.choices[0].message.content.strip()
        log.info("Compact summary (%d chars): %.80s...", len(summary_text), summary_text)
    except Exception as e:
        log.warning("Compact LLM summary failed: %s, falling back to raw", e)
        summary_parts = ["Exploration so far:"]
        for line in conv_lines:
            summary_parts.append(f"  {line}")
        if crop_files:
            crop_lines = ["\nCrops available:"]
            for cf in crop_files:
                cf_name = cf.rsplit("/", 1)[-1]
                crop_id = cf_name.replace("crop_", "").rsplit(".", 1)[0]
                crop_lines.append(f"  crop://{int(crop_id)}: {cf_name}")
            summary_parts.append("\n".join(crop_lines))
        if upscale_files:
            upscale_lines = ["\nUpscales available:"]
            for uf in upscale_files:
                uf_name = uf.rsplit("/", 1)[-1]
                us_id = uf_name.replace("upscale_", "").rsplit(".", 1)[0]
                upscale_lines.append(f"  upscale://{int(us_id)}: {uf_name}")
            summary_parts.append("\n".join(upscale_lines))
        summary_text = "\n".join(summary_parts)

    outcome_messages.append({"role": "user", "content": summary_text})


    # Strip image_url parts from messages BEFORE the compact-calling assistant
    strip_end = compact_assistant_idx if compact_assistant_idx is not None else len(state["messages"]) - 1
    for mi in range(strip_end):
        if state["messages"][mi].get("role") == "user":
            content = state["messages"][mi].get("content")
            if isinstance(content, list):
                state["messages"][mi]["content"] = [
                    p for p in content
                    if isinstance(p, dict) and p.get("type") != "image_url"
                ]

    tool_result = "Context compacted. Summary prepared, images removed. Use crop://N or upscale://N to re-view crops; use show_frame at @high resolution to re-examine frames."
    outcome_messages.append({
        "role": "tool", "tool_call_id": tc_id, "content": tool_result,
    })


    return {"crops_preserved": len(crop_files), "upscales_preserved": len(upscale_files),
            "messages": outcome_messages,
            "strip_images_before": compact_assistant_idx}



def _resolve_source_key(source: str, agent_dir: str, frames_dir: str) -> str | None:
    """Resolve a source URI to its stored S3 key. Returns None for unknown/unstored."""
    if source.startswith("frame://"):
        spec = source[len("frame://"):]
        try:
            return f"{frames_dir}/frames/frame_{int(spec):03d}.jpg"
        except ValueError:
            return None
    if source.startswith("crop://"):
        try:
            return f"{agent_dir}/crop_{int(source[len('crop://'):]):03d}.jpg"
        except ValueError:
            return None
    if source.startswith("upscale://"):
        try:
            return f"{agent_dir}/upscale_{int(source[len('upscale://'):]):03d}.jpg"
        except ValueError:
            return None
    if source.startswith("snapshot://") or source == "":
        return f"{frames_dir}/snapshot.jpg"
    return None


@activity.defn(name="tool_upscale")
async def tool_upscale_activity(arg: dict) -> dict:
    """Upscale an image 4x via the office GPU API. Shows result inline + stores as upscale://N."""
    tool_args = arg.get("args", {})
    source = tool_args.get("source", "snapshot://")
    model = tool_args.get("model", "swinir-psnr")
    if model not in ("swinir-psnr", "realesrgan"):
        model = "swinir-psnr"
    msg_path = arg["msg_path"]
    state, agent_dir = _load_state(msg_path)
    frames_dir = str(Path(agent_dir).parent)
    tc_id = _find_tc_id(state, "upscale")
    outcome_messages = []

    s3_key = _resolve_source_key(source, agent_dir, frames_dir)
    image_bytes = _s3_get(s3_key) if s3_key else None
    if image_bytes is None:
        if tc_id:
            outcome_messages.append({
                "role": "tool", "tool_call_id": tc_id,
                "content": f"Source not found or unsupported: {source}",
            })
        return {"source": source, "error": "source_not_found", "messages": outcome_messages}

    # Crop-first gate: reject inputs too large to be a "tight crop".
    max_input_px = int(os.environ.get("UPSCALE_MAX_INPUT_PX", "768"))
    src_img = Image.open(_io.BytesIO(image_bytes))
    if max(src_img.size) > max_input_px:
        if tc_id:
            outcome_messages.append({
                "role": "tool", "tool_call_id": tc_id,
                "content": (
                    f"{source} is {src_img.width}x{src_img.height} — too large to upscale "
                    f"(limit {max_input_px}px). First crop() a TIGHT region around the "
                    f"specific detail (plate, face, text), then upscale that crop://N."
                ),
            })
        return {"source": source, "error": "input_too_large",
                "width": src_img.width, "height": src_img.height, "messages": outcome_messages}

    # POST multipart to upscale API
    upscale_url = os.environ.get("UPSCALE_API_URL", "http://office.ts.2143.me:7870")
    boundary = "----UpscaleBoundary"
    body = (
        f"--{boundary}\r\n"
        f'Content-Disposition: form-data; name="file"; filename="image.jpg"\r\n'
        f"Content-Type: image/jpeg\r\n\r\n"
    ).encode() + image_bytes + f"\r\n--{boundary}\r\n".encode()
    body += (
        f'Content-Disposition: form-data; name="model"\r\n\r\n{model}\r\n'
        f"--{boundary}--\r\n"
    ).encode()
    req = urllib.request.Request(
        f"{upscale_url}/upscale", data=body,
        headers={"Content-Type": f"multipart/form-data; boundary={boundary}"},
    )
    def _call_api() -> bytes:
        with urllib.request.urlopen(req, timeout=50) as resp:
            if resp.status != 200:
                raise RuntimeError(f"HTTP {resp.status}: {resp.read()[:200]!r}")
            return resp.read()

    result_bytes = None
    last_err = None
    for attempt, delay in ((1, 2), (2, 5), (3, 0)):
        try:
            result_bytes = await asyncio.to_thread(_call_api)
            break
        except Exception as e:
            last_err = e
            if delay:
                await asyncio.sleep(delay)

    if result_bytes is None:
        # Self-report — NEVER raise: an ActivityError would kill the whole workflow.
        log.warning("Upscale API failed for %s: %s", source, last_err)
        if tc_id:
            outcome_messages.append({
                "role": "tool", "tool_call_id": tc_id,
                "content": (
                    f"Upscale service unavailable ({last_err}). "
                    f"Continue the analysis without upscaling — use crop/@max instead."
                ),
            })
        return {"source": source, "error": "api_unavailable", "messages": outcome_messages}

    # Normalize to JPEG + get dims
    img = Image.open(_io.BytesIO(result_bytes)).convert("RGB")
    buf = _io.BytesIO()
    img.save(buf, "JPEG", quality=92)
    jpeg_bytes = buf.getvalue()

    # Store as upscale://N
    up_idx = len([k for k in _s3_list(agent_dir + "/")
                  if k.rsplit("/", 1)[-1].startswith("upscale_")]) + 1
    fname = f"upscale_{up_idx:03d}.jpg"
    _s3_put(f"{agent_dir}/{fname}", jpeg_bytes)

    # Show inline to the model
    outcome_messages.append({
        "role": "user",
        "content": [
            {"type": "image_url", "image_url": {"url": f"[[{fname}]]"}},
            {"type": "text", "text":
                f"Upscaled {source} 4x with {model} → {img.width}x{img.height}. "
                f"Stored as upscale://{up_idx}."},
        ],
    })
    return {"source": source, "model": model, "upscale_id": up_idx,
            "width": img.width, "height": img.height, "messages": outcome_messages}

@activity.defn(name="tool_set_description")
async def tool_set_description_activity(arg: dict) -> dict:
    """Append the final description/confidence to messages.json."""
    msg_path = arg["msg_path"]
    state, agent_dir = _load_state(msg_path)

    outcome_messages = []

    # description and confidence are pre-validated in run_genai_turn_activity
    # and passed through the workflow — re-extract from state for safety
    assistant_msg = state["messages"][-1]
    for tc in assistant_msg.get("tool_calls", []):
        if tc.get("function", {}).get("name") == "set_description":
            args = tc.get("function", {}).get("arguments", {})
            if isinstance(args, str):
                try:
                    args = json.loads(args)
                except json.JSONDecodeError:
                    args = {}
            description = args.get("description", "")
            confidence = args.get("confidence", "medium")
            tc_id = tc.get("id")
            tool_result = f"Description set. Confidence: {confidence}."
            if tc_id:
                outcome_messages.append({
                    "role": "tool", "tool_call_id": tc_id, "content": tool_result,
                })
            break

    return {"description_set": True, "confidence": confidence, "messages": outcome_messages}


@activity.defn(name="summarize_agent")
async def summarize_agent_activity(stats: dict) -> str | None:
    """Summarize agentic strategy. Separate activity for Temporal audit trail."""
    from openai import OpenAI

    event_id = stats["event_id"]
    model = stats["model"]
    provider_path = stats["provider_path"]

    try:
        provider_cfg = load_json(provider_path)
    except (OSError, json.JSONDecodeError) as e:
        log.error("Failed to load provider config in summarize_agent: %s", e)
        raise RuntimeError(f"Failed to load config: {e}") from e

    client, model_name = _resolve_provider(provider_cfg, model, timeout=30.0)

    prompt = (
        f"Answer in one sentence: model {model}, "
        f"viewed {stats['turns_low']} low-res + {stats['turns_high']} high-res + "
        f"{stats.get('turns_max', 0)} max-res + {stats.get('turns_transcode', 0)} transcoded "
        f"frames in {stats['turns']} turns. "
        "What the agent found, what search pattern it used."
    )

    def _call_summarize() -> str | None:
        response = client.chat.completions.create(
            model=model_name,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.0,
        )
        return response.choices[0].message.content

    try:
        summary = await _run_with_heartbeat(_call_summarize)
        if summary:
            summary = f"Agent: {summary}"
            log.info("Agent summary for %s: %s", event_id, summary)
            try:
                agent_prefix = _s3_agent_prefix(event_id)
                _s3_put(f"{agent_prefix}/summary.txt", summary.encode())
                trace_key = f"{agent_prefix}/trace.txt"
                existing = _s3_read_text(trace_key)
                if existing is not None:
                    _s3_put(trace_key, (existing + f"\n\n{summary}").encode())
            except Exception as e:
                pass
            return summary
    except Exception as e:
        log.warning("Agent summary call failed for %s: %s", event_id, e)
    return None



def _atomic_write(path: str, data: dict) -> None:
    """Write JSON to S3 (if path starts with 'events/') or disk atomically."""
    if path.startswith("events/"):
        _s3_put(path, json.dumps(data, default=str).encode())
    else:
        tmp = path + ".tmp"
        with open(tmp, "w") as f:
            json.dump(data, f, default=str)
        os.replace(tmp, path)



def _load_state(msg_path: str) -> tuple[dict, str]:
    """Read messages.json from S3 or local disk. Returns (state, agent_dir)."""
    if msg_path.startswith("events/"):
        data = _s3_read_text(msg_path)
        if data is None:
            raise FileNotFoundError(f"State not found at {msg_path}")
        state = json.loads(data)
    else:
        with open(msg_path) as f:
            state = json.load(f)
    return state, state["agent_dir"]


def _find_tc_id(state: dict, tool_name: str) -> str | None:
    """Find the tool_call_id in the most recent assistant message matching tool_name."""
    msgs = state.get("messages", [])
    if not msgs:
        return None
    assistant_msg = msgs[-1]
    for tc in assistant_msg.get("tool_calls", []):
        if tc.get("function", {}).get("name") == tool_name:
            return tc.get("id")
    return None


def _deserialize_messages(messages: list, agent_dir: str) -> list:
    """Convert [[filename]] refs in messages to base64 data URIs.
    Reads image bytes from S3 (if agent_dir starts with 'events/') or disk.
    Does NOT mutate — returns new list.
    """
    is_s3 = agent_dir.startswith("events/")
    result = copy.deepcopy(messages)
    for m in result:
        content = m.get("content")
        if isinstance(content, list):
            for part in content:
                if isinstance(part, dict) and part.get("type") == "image_url":
                    url = part.get("image_url", {}).get("url", "")
                    if url.startswith("[[") and url.endswith("]]"):
                        fname = url[2:-2]
                        img_key = f"{agent_dir}/{fname}"
                        raw = _s3_get(img_key) if is_s3 else None
                        if raw is None and not is_s3:
                            p = Path(agent_dir) / fname
                            raw = p.read_bytes() if p.exists() else None
                        if raw:
                            b64 = base64.b64encode(raw).decode("ascii")
                            part["image_url"]["url"] = f"data:image/jpeg;base64,{b64}"
    return result




