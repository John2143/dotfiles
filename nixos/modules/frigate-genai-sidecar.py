def update_event_description(event_id: str, description: str) -> bool:
    """Write description back to Frigate's event."""
    url = _frigate_url(f"/api/events/{event_id}/description")
    payload = json.dumps({"description": description}).encode()
    try:
        req = urllib.request.Request(
            url, data=payload, method="POST",
            headers={"Content-Type": "application/json"},
        )
        with urllib.request.urlopen(req, timeout=10) as resp:
            return resp.status == 200
    except Exception as e:
        log.error("Failed to update event %s: %s", event_id, e)
        return False





_RES_LEVELS = {
    "tiny": 320,
    "low": 640,
    "med": 960,
    "high": 1280,
    "max": None,  # special-cased: return original size
}

def _resolution_pixels(resolution: str, size: tuple[int, int]) -> tuple[int, int]:
    """Compute target pixel dimensions for a given resolution level."""
    w, h = size
    tw = _RES_LEVELS.get(resolution)
    if tw is None:
        return w, h
    return tw, int(tw * h / w)


def _resize_to(img: Image.Image, target_width: int) -> Image.Image:
    """Resize PIL image to target width, preserving aspect ratio."""
    if img.width <= target_width:
        return img.copy()
    ratio = target_width / img.width
    new_h = int(img.height * ratio)
    return img.resize((target_width, new_h), Image.LANCZOS)




