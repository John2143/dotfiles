def _serialize_for_log(
    messages: list,
    total_cost: dict,
    event_id: str,
    camera: str,
    label: str,
    model: str,
    num_turns: int,
    turns_low: int,
    turns_high: int,
    turns_max: int = 0,
    turns_transcode: int = 0,
    summary: str | None = None,
) -> dict:
    """Convert messages to a JSON-serializable structure with inline images."""
    import base64 as _b64
    result = {
        "event_id": event_id,
        "camera": camera,
        "label": label,
        "model": model,
        "turns": num_turns,
        "frames_viewed": {
            "low": turns_low, "high": turns_high,
            "max": turns_max, "transcode": turns_transcode,
        },
        "total_cost": total_cost,
    }
    if summary:
        result["summary"] = summary

    serialized = []
    for m in messages:
        role = m.get("role", "unknown")
        entry = {"role": role}

        if "tool_calls" in m:
            entry["tool_calls"] = [
                {
                    "id": tc.get("id"),
                    "name": tc.get("function", {}).get("name", ""),
                    "arguments": tc.get("function", {}).get("arguments", ""),
                }
                for tc in m.get("tool_calls", [])
            ]
        elif "tool_call_id" in m:
            entry["tool_call_id"] = m.get("tool_call_id")
            entry["content"] = m.get("content", "")
        elif role == "assistant":
            entry["content"] = m.get("content", "")
        elif role == "user":
            content = m.get("content", "")
            if isinstance(content, list):
                parts = []
                for part in content:
                    if isinstance(part, dict) and part.get("type") == "image_url":
                        url = part.get("image_url", {}).get("url", "")
                        if url.startswith("data:"):
                            parts.append({"type": "image_url", "data": url})
                        else:
                            parts.append(part)
                    else:
                        parts.append(part)
                entry["content"] = parts
            else:
                entry["content"] = content
        else:
            entry["content"] = str(m.get("content", ""))

        serialized.append(entry)

    result["messages"] = serialized
    return result

def _atomic_write(path: str, data: dict) -> None:
    """Write JSON to disk atomically via .tmp → rename."""
    import json as _json
    tmp = path + ".tmp"
    with open(tmp, "w") as f:
        _json.dump(data, f, default=str)
    os.replace(tmp, path)

def _serialize_messages(messages: list, agent_dir: str) -> list:
    """Convert base64 data URIs in messages to agent:// refs.
    Writes image bytes to agent_dir. Mutates messages in place.
    Returns the modified messages list.
    """
    import base64 as _b64
    import json as _json
    agent_path = Path(agent_dir)
    agent_path.mkdir(parents=True, exist_ok=True)
    img_counter = [0]

    def _save_image(b64_data: str) -> str:
        """Save base64 data to agent_dir, return agent:// ref."""
        img_counter[0] += 1
        fname = f"tool_{img_counter[0]:03d}.jpg"
        raw = _b64.b64decode(b64_data)
        (agent_path / fname).write_bytes(raw)
        return f"agent://{fname}"

    for m in messages:
        content = m.get("content")
        if isinstance(content, list):
            for part in content:
                if isinstance(part, dict) and part.get("type") == "image_url":
                    url = part.get("image_url", {}).get("url", "")
                    if url.startswith("data:image/jpeg;base64,"):
                        b64 = url[len("data:image/jpeg;base64,"):]
                        part["image_url"]["url"] = _save_image(b64)
    return messages

def _deserialize_messages(messages: list, agent_dir: str) -> list:
    """Convert agent:// refs in messages to base64 data URIs.
    Reads image bytes from agent_dir. Does NOT mutate — returns new list.
    """
    import base64 as _b64
    import json as _json
    agent_path = Path(agent_dir)
    result = _json.loads(_json.dumps(messages, default=str))
    for m in result:
        content = m.get("content")
        if isinstance(content, list):
            for part in content:
                if isinstance(part, dict) and part.get("type") == "image_url":
                    url = part.get("image_url", {}).get("url", "")
                    if url.startswith("agent://"):
                        fname = url[len("agent://"):]
                        img_path = agent_path / fname
                        if img_path.exists():
                            raw = img_path.read_bytes()
                            b64 = _b64.b64encode(raw).decode("ascii")
                            part["image_url"]["url"] = f"data:image/jpeg;base64,{b64}"
    return result




