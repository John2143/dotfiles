async def _run_with_heartbeat(func, *args, interval: float = 5.0):
    """Run a sync function in a thread, heartbeating every `interval` seconds.

    Prevents Temporal from cancelling the activity while the thread is busy
    (e.g. waiting for a slow LLM). On cancellation the inner task is also
    cancelled to avoid orphaning the thread.
    """
    task = asyncio.create_task(asyncio.to_thread(func, *args))
    try:
        while not task.done():
            try:
                await asyncio.wait_for(asyncio.shield(task), timeout=interval)
            except TimeoutError:
                activity.heartbeat()
        return task.result()
    except asyncio.CancelledError:
        task.cancel()
        raise
@activity.defn(name="select_model")
async def select_model_activity(input_data: dict) -> str:
    """Select the LLM model for this workflow. Activity so it's visible
    in Temporal history and can run on any worker (distributed-safe)."""
    import random
    provider_cfg = load_json(input_data.get("provider_path",
        "/var/lib/frigate-genai-sidecar/provider.json"))
    models = provider_cfg.get("model", ["gemini/gemini-2.5-flash"])
    if not isinstance(models, list):
        models = [models]

    paused = input_data.get("paused-ollama", False)
    if paused:
        gemini_models = [m for m in models if m.startswith("gemini/")]
        model = gemini_models[0] if gemini_models else "gemini/gemini-2.5-flash"
        log.warning("Paused ollama — forcing gemini model: %s", model)
    else:
        model = random.choice(models)

    log.info("Selected model %s (paused=%s)", model, paused)
    return model
@activity.defn(name="transcode_into_parts")
async def transcode_into_parts_activity(input_data: dict) -> tuple[str, int]:
    """Extract frames + proxy MP4 via ffmpeg, save to disk. Returns (frames_dir, frame_count)."""
    camera = input_data["camera"]
    start_time = input_data["start_time"]
    end_time = input_data["end_time"]
    event_id = input_data["event_id"]

    duration = end_time - start_time
    fps = max(1, min(4, 4 / max(1, duration)))
    max_frames = max(4, min(300, int(duration * fps)))

    frames_dir = FRAMES_DIR / event_id
    frames_dir_str = str(frames_dir)
    proxy_path = str(frames_dir / "proxy.mp4")
    log.info(
        "Activity transcode_into_parts: event=%s camera=%s duration=%.1fs max_frames=%d dir=%s",
        event_id, camera, duration, max_frames, frames_dir,
    )

    frames = await _run_with_heartbeat(
        transcode_into_parts, camera, start_time, end_time, fps, max_frames, proxy_path,
    )
    if not frames:
        raise ApplicationError(
            f"Recording not ready for {event_id}", non_retryable=False,
        )

    frames_dir.mkdir(parents=True, exist_ok=True)
    for i, frame_bytes in enumerate(frames):
        (frames_dir / f"frame_{i:03d}.jpg").write_bytes(frame_bytes)

    proxy_size = Path(proxy_path).stat().st_size if Path(proxy_path).exists() else 0
    log.info("Extracted %d ffmpeg frames + proxy (%d bytes) to %s", len(frames), proxy_size, frames_dir)
    return frames_dir_str, len(frames)
