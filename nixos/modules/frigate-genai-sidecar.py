def _resize_frame(img_bytes: bytes, max_width: int, quality: int = 60) -> bytes:
    """Resize a JPEG frame to max_width, preserving aspect ratio. Returns JPEG bytes."""
    from io import BytesIO
    img = Image.open(BytesIO(img_bytes))
    if img.mode != "RGB":
        img = img.convert("RGB")
    if img.width > max_width:
        img = ImageOps.exif_transpose(img)
        img.thumbnail((max_width, max_width))
    buf = BytesIO()
    img.save(buf, format="JPEG", quality=quality)
    return buf.getvalue()


def _load_frames(
    frame_files: list[Path],
    start: int,
    end: int,
    resolution: str = "low",
) -> list[bytes]:
    """Load and optionally resize frames [start, end] inclusive.
    resolution: 'low'=640, 'high'=1280, 'max'=source (no resize)."""
    if resolution == "max":
        max_width = None
        quality = 85
    elif resolution == "high":
        max_width = 1280
        quality = 85
    else:
        max_width = 640
        quality = 60
    result = []
    for i in range(max(0, start), min(end + 1, len(frame_files))):
        data = frame_files[i].read_bytes()
        if max_width:
            data = _resize_frame(data, max_width, quality)
        result.append(data)
    return result


def _resolution_pixels(resolution: str, size: tuple[int, int]) -> tuple[int, int]:
    """Compute target pixel dimensions for a given resolution."""
    w, h = size
    if resolution == "max":
        return w, h
    elif resolution == "high":
        tw = min(w, 1280)
        return tw, int(tw * h / w)
    else:
        tw = min(w, 640)
        return tw, int(tw * h / w)


def _resize_to(img: Image.Image, target_width: int) -> Image.Image:
    """Resize PIL image to target width, preserving aspect ratio."""
    if img.width <= target_width:
        return img.copy()
    ratio = target_width / img.width
    new_h = int(img.height * ratio)
    return img.resize((target_width, new_h), Image.LANCZOS)
