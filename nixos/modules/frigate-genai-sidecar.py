def cleanup_old_files(base_dir: Path, subdir: str, max_age_minutes: int) -> int:
    """Delete files and dirs older than max_age_minutes in base_dir/subdir.
    Returns count of removed items."""
    import shutil

    target = base_dir / subdir
    if not target.exists():
        return 0
    cutoff = time.time() - (max_age_minutes * 60)
    removed = 0
    for entry in target.iterdir():
        try:
            if entry.stat().st_mtime < cutoff:
                if entry.is_dir():
                    shutil.rmtree(entry)
                else:
                    entry.unlink()
                removed += 1
        except OSError:
            pass
    return removed

# ── frame extraction ────────────────────────────────────────────────

def transcode_into_parts(
    camera: str,
    start_time: float,
    end_time: float,
    fps: float,
    max_frames: int = 30,
    proxy_path: str = "",


) -> list[bytes]:
    """
    Extract JPEG frames at source resolution from Frigate recording via HLS.
    If proxy_path is given, simultaneously produce a local H.264 proxy MP4
    for fast in-process transcoding — one HLS pull, two outputs.

    Returns list of JPEG byte strings, chronologically ordered.
    """
    base_url = os.environ.get("FRIGATE_BASE_URL", "http://localhost:5000")
    hls_url = (
        f"{base_url}/vod/{camera}"
        f"/start/{int(start_time)}/end/{int(math.ceil(end_time))}"
        f"/index.m3u8"
    )
    duration = end_time - start_time

    log.info(
        "Extracting up to %d frames at %.1ffps from %s (%.1fs clip)",
        max_frames, fps, hls_url, duration,
    )

    with tempfile.TemporaryDirectory(prefix="frigate-genai-") as tmp:
        cmd: list[str] = [
            "ffmpeg",
            "-y",
            "-loglevel", "error",
            "-i", hls_url,
            "-map", "0:v",
            "-vf", f"fps={fps}",
            "-frames:v", str(max_frames),
            "-q:v", "3",
            f"{tmp}/frame_%03d.jpg",
        ]
        # Dual output: simultaneously produce H.264 proxy MP4 via GPU (nvenc)
        if proxy_path:
            cmd += [
                "-map", "0:v",
                "-vf", f"fps={fps},scale='min(1920,iw)':-2,format=yuv420p",
                "-c:v", "h264_nvenc",
                "-preset", "p1",
                "-cq", "23",
                "-an",
                proxy_path,
            ]
            log.info("Producing proxy MP4 at %s", proxy_path)
        try:
            result = subprocess.run(
                cmd, check=True, capture_output=True, text=True,
            )
        except subprocess.CalledProcessError as e:
            stderr = (e.stderr or "").strip()
            if "404 Not Found" in stderr or "Connection refused" in stderr:
                log.debug("ffmpeg: recording not ready for %s", hls_url)
            else:
                log.error("ffmpeg failed: %s", stderr)
            return []

        frames = []
        for p in sorted(Path(tmp).glob("frame_*.jpg")):
            frames.append(p.read_bytes())

        if not frames:
            log.warning("No frames extracted")
        else:
            log.info("Extracted %d frames", len(frames))

        return frames
