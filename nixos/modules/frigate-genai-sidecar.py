@activity.defn(name="summarize_agent")
async def summarize_agent_activity(stats: dict) -> str | None:
    """Summarize agentic strategy. Separate activity for Temporal audit trail."""
    from openai import OpenAI

    event_id = stats["event_id"]
    model = stats["model"]
    provider_path = stats["provider_path"]

    try:
        provider_cfg = load_json(provider_path)
    except (OSError, json.JSONDecodeError) as e:
        log.error("Failed to load provider config in summarize_agent: %s", e)
        raise RuntimeError(f"Failed to load config: {e}") from e

    is_ollama = model.startswith("ollama/")
    if is_ollama:
        base_url = provider_cfg.get("ollama_base_url", "http://office.ts.2143.me:11434/v1")
        api_key = os.environ.get("OLLAMA_API_KEY", "ollama")
        model_name = model[len("ollama/"):]
    else:
        base_url = provider_cfg.get("base_url", os.environ.get("OPENAI_BASE_URL", ""))
        api_key = os.environ.get(provider_cfg.get("api_key_env", ""), "")
        if not api_key:
            log.error("API key not found in env var %s", provider_cfg.get("api_key_env"))
            raise RuntimeError("API key not configured")
        model_name = model
    client = OpenAI(api_key=api_key, base_url=base_url, timeout=30.0)

    prompt = (
        f"Answer in one sentence: model {model}, "
        f"viewed {stats['turns_low']} low-res + {stats['turns_high']} high-res + "
        f"{stats.get('turns_max', 0)} max-res + {stats.get('turns_transcode', 0)} transcoded "
        f"frames in {stats['turns']} turns. "
        "What the agent found, what search pattern it used."
    )

    def _call_summarize() -> str | None:
        response = client.chat.completions.create(
            model=model_name,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.0,
        )
        return response.choices[0].message.content

    try:
        summary = await _run_with_heartbeat(_call_summarize)
        if summary:
            summary = f"Agent: {summary}"
            log.info("Agent summary for %s: %s", event_id, summary)
            try:
                AGENT_LOGS_DIR.mkdir(parents=True, exist_ok=True)
                (AGENT_LOGS_DIR / f"{event_id}-summary.txt").write_text(summary)
                trace_path = AGENT_LOGS_DIR / f"{event_id}-trace.txt"
                if trace_path.exists():
                    trace_path.write_text(trace_path.read_text() + f"\n\n{summary}")
            except Exception:
                pass
            return summary
    except Exception as e:
        log.warning("Agent summary call failed for %s: %s", event_id, e)
    return None


def _serialize_for_log(
    messages: list,
    total_cost: dict,
    event_id: str,
    camera: str,
    label: str,
    model: str,
    num_turns: int,
    turns_low: int,
    turns_high: int,
    turns_max: int = 0,
    turns_transcode: int = 0,
    summary: str | None = None,
) -> dict:
    """Convert messages to a JSON-serializable structure with inline images."""
    import base64 as _b64
    result = {
        "event_id": event_id,
        "camera": camera,
        "label": label,
        "model": model,
        "turns": num_turns,
        "frames_viewed": {
            "low": turns_low, "high": turns_high,
            "max": turns_max, "transcode": turns_transcode,
        },
        "total_cost": total_cost,
    }
    if summary:
        result["summary"] = summary

    serialized = []
    for m in messages:
        role = m.get("role", "unknown")
        entry = {"role": role}

        if "tool_calls" in m:
            entry["tool_calls"] = [
                {
                    "id": tc.get("id"),
                    "name": tc.get("function", {}).get("name", ""),
                    "arguments": tc.get("function", {}).get("arguments", ""),
                }
                for tc in m.get("tool_calls", [])
            ]
        elif "tool_call_id" in m:
            entry["tool_call_id"] = m.get("tool_call_id")
            entry["content"] = m.get("content", "")
        elif role == "assistant":
            entry["content"] = m.get("content", "")
        elif role == "user":
            content = m.get("content", "")
            if isinstance(content, list):
                parts = []
                for part in content:
                    if isinstance(part, dict) and part.get("type") == "image_url":
                        url = part.get("image_url", {}).get("url", "")
                        if url.startswith("data:"):
                            parts.append({"type": "image_url", "data": url})
                        else:
                            parts.append(part)
                    else:
                        parts.append(part)
                entry["content"] = parts
            else:
                entry["content"] = content
        else:
            entry["content"] = str(m.get("content", ""))

        serialized.append(entry)

    result["messages"] = serialized
    return result

def _atomic_write(path: str, data: dict) -> None:
    """Write JSON to disk atomically via .tmp → rename."""
    import json as _json
    tmp = path + ".tmp"
    with open(tmp, "w") as f:
        _json.dump(data, f, default=str)
    os.replace(tmp, path)


def _load_state(msg_path: str) -> tuple[dict, str]:
    """Read messages.json and return (state, agent_dir)."""
    import json as _json
    with open(msg_path) as f:
        state = _json.load(f)
    return state, state["agent_dir"]


def _find_tc_id(state: dict, tool_name: str) -> str | None:
    """Find the tool_call_id in the most recent assistant message matching tool_name."""
    assistant_msg = state["messages"][-1]
    for tc in assistant_msg.get("tool_calls", []):
        if tc.get("function", {}).get("name") == tool_name:
            return tc.get("id")
    return None


def _deserialize_messages(messages: list, agent_dir: str) -> list:
    """Convert [[filename]] refs in messages to base64 data URIs.
    Reads image bytes from agent_dir. Does NOT mutate — returns new list.
    """
    import base64 as _b64
    import json as _json
    agent_path = Path(agent_dir)
    result = _json.loads(_json.dumps(messages, default=str))
    for m in result:
        content = m.get("content")
        if isinstance(content, list):
            for part in content:
                if isinstance(part, dict) and part.get("type") == "image_url":
                    url = part.get("image_url", {}).get("url", "")
                    if url.startswith("[[") and url.endswith("]]"):
                        fname = url[2:-2]
                        img_path = agent_path / fname
                        if img_path.exists():
                            raw = img_path.read_bytes()
                            b64 = _b64.b64encode(raw).decode("ascii")
                            part["image_url"]["url"] = f"data:image/jpeg;base64,{b64}"
    return result




