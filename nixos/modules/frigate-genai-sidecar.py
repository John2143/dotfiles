# ── config ──────────────────────────────────────────────────────────

def load_json(path: str) -> dict:
    with open(path) as f:
        return json.load(f)


# ── frame extraction ────────────────────────────────────────────────

def extract_frames(
    camera: str,
    start_time: float,
    end_time: float,
    fps: float,
    max_frames: int = 30,
    max_width: int = 1280,
    jpg_quality: int = 85,
) -> list[bytes]:
    """
    Extract JPEG frames from Frigate recording via HLS.

    Returns list of JPEG byte strings, chronologically ordered.
    """
    base_url = os.environ.get("FRIGATE_BASE_URL", "http://localhost:5000")
    # Frigate VOD endpoint: /vod/<camera>/start/<ts>/end/<ts>/index.m3u8
    hls_url = (
        f"{base_url}/vod/{camera}"
        f"/start/{int(start_time)}/end/{int(math.ceil(end_time))}"
        f"/index.m3u8"
    )
    duration = end_time - start_time

    log.info(
        "Extracting up to %d frames at %.1ffps from %s (%.1fs clip)",
        max_frames, fps, hls_url, duration,
    )

    with tempfile.TemporaryDirectory(prefix="frigate-genai-") as tmp:
        cmd = [
            "ffmpeg",
            "-y",
            "-loglevel", "error",
            "-i", hls_url,
            "-vf", f"fps={fps},scale=1280:-2",
            "-frames:v", str(max_frames),
            "-q:v", str(max(1, min(31, int(31 - 31 * jpg_quality / 100)))),
            f"{tmp}/frame_%03d.jpg",
        ]
        try:
            subprocess.run(cmd, check=True, timeout=60)
        except subprocess.CalledProcessError as e:
            log.error("ffmpeg failed: %s", e)
            return []
        except subprocess.TimeoutExpired:
            log.error("ffmpeg timed out extracting frames")
            return []

        # Collect frames in order
        frames = []
        for p in sorted(Path(tmp).glob("frame_*.jpg")):
            frames.append(p.read_bytes())

        if not frames:
            log.warning("No frames extracted")
        else:
            log.info("Extracted %d frames", len(frames))

        return frames


def fetch_snapshot(event_id: str) -> bytes | None:
    """Fetch the event snapshot (close-up of tracked object)."""
    base_url = os.environ.get("FRIGATE_BASE_URL", "http://localhost:5000")
    url = f"{base_url}/api/events/{event_id}/snapshot.jpg"
    try:
        req = urllib.request.Request(url)
        with urllib.request.urlopen(req, timeout=15) as resp:
            return resp.read()
    except Exception as e:
        log.warning("Failed to fetch snapshot for %s: %s", event_id, e)
        return None


# ── Gemini provider ─────────────────────────────────────────────────

def call_gemini(
    prompt_text: str,
    images: list[bytes],
    snapshot: bytes | None,
    label: str,
    provider_cfg: dict,
) -> str | None:
    """
    Call Gemini 2.5 Flash with sequential contents:
      [prompt_text, frame1...frameN, transition_text, snapshot]
    """
    from google import genai
    from google.genai import types

    api_key = os.environ.get(provider_cfg.get("api_key_env", ""), "")
    if not api_key:
        log.error("Gemini API key not found in env var %s", provider_cfg.get("api_key_env"))
        return None

    client = genai.Client(api_key=api_key)

    contents: list = [prompt_text]

    # Add all frames as image parts
    for img in images:
        contents.append(types.Part.from_bytes(data=img, mime_type="image/jpeg"))

    # Add snapshot as detail close-up with transition text
    if snapshot:
        contents.append(
            f"The next image is a close-up of the {label}. "
            "Use it for appearance detail (clothing, items, expression); "
            "use the preceding full frames for scene context, movement, "
            "and event progression."
        )
        contents.append(types.Part.from_bytes(data=snapshot, mime_type="image/jpeg"))

    log.info("Calling %s with %d images + 1 snapshot", provider_cfg["model"], len(images))

    try:
        response = client.models.generate_content(
            model=provider_cfg["model"],
            contents=contents,
        )
        return response.text, provider_cfg["model"]
    except Exception as e:
        log.error("Gemini call failed: %s", e)
        return None, None

# ── LiteLLM provider ────────────────────────────────────────────────

def call_litellm(
    prompt_text: str,
    images: list[bytes],
    snapshot: bytes | None,
    label: str,
    provider_cfg: dict,
) -> str | None:
    """
    Call LiteLLM (OpenAI-compatible) with images as base64 data URIs.
    Content order: [prompt_text, frame1...frameN, transition_text, snapshot]
    """
    import base64
    from openai import OpenAI

    api_key = os.environ.get(provider_cfg.get("api_key_env", ""), "")
    if not api_key:
        log.error("LiteLLM API key not found in env var %s", provider_cfg.get("api_key_env"))
        return None

    model = provider_cfg.get("model", "gemini/gemini-2.5-flash")
    if isinstance(model, list):
        import random
        model = random.choice(model)
    base_url = provider_cfg.get("base_url", os.environ.get("OPENAI_BASE_URL", ""))
    client = OpenAI(api_key=api_key, base_url=base_url)


    content: list[dict] = [{"type": "text", "text": prompt_text}]

    # Add all frames as image parts
    for img in images:
        b64 = base64.b64encode(img).decode()
        content.append({
            "type": "image_url",
            "image_url": {"url": f"data:image/jpeg;base64,{b64}"},
        })

    # Add snapshot as detail close-up with transition text
    if snapshot:
        content.append({
            "type": "text",
            "text": (
                f"The next image is a close-up of the {label}. "
                "Use it for appearance detail (clothing, items, expression); "
                "use the preceding full frames for scene context, movement, "
                "and event progression."
            ),
        })
        b64 = base64.b64encode(snapshot).decode()
        content.append({
            "type": "image_url",
            "image_url": {"url": f"data:image/jpeg;base64,{b64}"},
        })

    log.info("Calling LiteLLM %s with %d images + 1 snapshot", model, len(images))

    try:
        response = client.chat.completions.create(
            model=model,
            messages=[{"role": "user", "content": content}],
            timeout=600,
        )
        return response.choices[0].message.content, model
    except Exception as e:
        log.error("LiteLLM call failed: %s", e)
        return None, None


# ── provider dispatch ───────────────────────────────────────────────

PROVIDERS = {
    "gemini": call_gemini,
    "litellm": call_litellm,
}


def call_provider(
    prompt_text: str,
    images: list[bytes],
    snapshot: bytes | None,
    label: str,
    provider_cfg: dict,
) -> tuple[str | None, str | None]:
    """Dispatch to the configured GenAI provider. Returns (description, model_used)."""
    provider_name = provider_cfg.get("provider", "gemini")
    fn = PROVIDERS.get(provider_name)
    if fn is None:
        log.error("Unknown provider: %s", provider_name)
        return None, None
    return fn(prompt_text, images, snapshot, label, provider_cfg)


# ── Frigate API ─────────────────────────────────────────────────────

def update_event_description(event_id: str, description: str) -> bool:
    """Write description back to Frigate's event."""
    base_url = os.environ.get("FRIGATE_BASE_URL", "http://localhost:5000")
    url = f"{base_url}/api/events/{event_id}/description"
    payload = json.dumps({"description": description}).encode()
    try:
        req = urllib.request.Request(
            url, data=payload, method="POST",
            headers={"Content-Type": "application/json"},
        )
        with urllib.request.urlopen(req, timeout=10) as resp:
            return resp.status == 200
    except Exception as e:
        log.error("Failed to update event %s: %s", event_id, e)
        return False


# ── prompt composition ──────────────────────────────────────────────

def compose_prompt(
    prompts: dict,
    camera: str,
    label: str,
    num_frames: int,
) -> str:
    """Compose the prompt from the Nix-generated template blocks."""
    base = prompts.get("base", "")
    camera_desc = prompts.get("camera", {}).get(camera, "")
    suffix = prompts.get("suffix", "")
    # Use object-specific hint if available, otherwise the label-specific prompt
    label_hint = prompts.get("label", {}).get(label, "")

    # Replace {label} placeholder in base
    base = base.replace("{label}", label)

    parts = [base]
    if camera_desc:
        parts.append(camera_desc)
    if label_hint:
        parts.append(label_hint)
    if suffix:
        parts.append(suffix)

    prompt = " ".join(parts)
    # Replace {label} in the assembled prompt too (suffix/camera may use it)
    prompt = prompt.replace("{label}", label)
    # Add frame count
    # Replace the "count frames" debug directive with actual frame info
    first_line = "FIRST: State exactly how many image frames you received."
    if first_line in prompt:
        prompt = prompt.replace(
            first_line,
            f"You are viewing {num_frames} chronological frames from a security camera, "
            f"at ~1-4 frames per second, plus one close-up detail image",
        )

    return prompt


# ── event processing ────────────────────────────────────────────────

class EventProcessor:
    """Processes Frigate events: extract frames, call GenAI, update description."""

    def __init__(self, prompts: dict, provider_cfg: dict):
        self.prompts = prompts
        self.provider_cfg = provider_cfg
        self.describe_labels: set[str] = set(prompts.get("describe", []))
        self.stats = {
            "events_processed": 0,
            "model_counts": {},
            "last_processed_time": None,
            "last_event": None,
            "total_genai_time": 0.0,
            "avg_genai_time": 0.0,
            "models_configured": provider_cfg.get("model", "unknown"),
            "provider": provider_cfg.get("provider", "unknown"),
            "started_at": __import__("time").strftime("%Y-%m-%dT%H:%M:%SZ", __import__("time").gmtime()),
            "mqtt_connected": False,
        }
        # Per-camera lock: only one active request per camera

    async def process(self, event: dict) -> None:
        """Process a single event (called from asyncio main loop)."""
        event_id = event.get("id")
        camera = event.get("camera", "")
        label = event.get("label", "")
        start_time = event.get("start_time", 0)
        end_time = event.get("end_time", start_time)
        has_clip = event.get("has_clip", False)

        # ── filters ──
        if not has_clip:
            log.debug("Event %s has no clip, skipping", event_id)
            return
        if not end_time:
            log.debug("Event %s has no end_time, skipping", event_id)
            return
        if label not in self.describe_labels:
            log.debug("Event %s label=%s not in describe list, skipping", event_id, label)
            return

        await self._process_event(event_id, camera, label, start_time, end_time)

    async def _process_event(
        self,
        event_id: str,
        camera: str,
        label: str,
        start_time: float,
        end_time: float,
    ) -> None:
        duration = end_time - start_time
        fps = max(1, min(4, 4 / max(1, duration)))
        max_frames = max(4, min(30, int(duration * fps)))

        log.info(
            "Processing event %s: camera=%s label=%s duration=%.1fs frames=%d fps=%.1f",
            event_id, camera, label, duration, max_frames, fps,
        )

        # 0. Brief delay for Frigate to finalize recording segments
        await asyncio.sleep(2)

        # 1. Extract frames from recording
        start_ts = time.monotonic()
        frames = await asyncio.to_thread(
            extract_frames, camera, start_time, end_time, fps, max_frames,
        )
        extract_time = time.monotonic() - start_ts
        log.info("Frame extraction took %.1fs", extract_time)

        if not frames:
            log.error("No frames extracted for event %s", event_id)
            return

        # 2. Fetch snapshot (close-up detail)
        snapshot = await asyncio.to_thread(fetch_snapshot, event_id)

        # 3. Compose prompt
        prompt = compose_prompt(self.prompts, camera, label, len(frames))

        # 4. Call GenAI provider
        genai_start = time.monotonic()
        description, model_used = await asyncio.to_thread(
            call_provider, prompt, frames, snapshot, label, self.provider_cfg,
        )
        genai_time = time.monotonic() - genai_start
        log.info("GenAI call took %.1fs", genai_time)

        if not description:
            log.error("No description returned for event %s", event_id)
            return

        # 5. Write description to Frigate
        ok = await asyncio.to_thread(update_event_description, event_id, description)
        if ok:
            self.stats["events_processed"] += 1
            self.stats["last_processed_time"] = __import__("time").strftime("%Y-%m-%dT%H:%M:%SZ", __import__("time").gmtime())
            self.stats["last_event"] = f"{camera}/{label}/{event_id[:12]}"
            self.stats["total_genai_time"] += genai_time
            self.stats["avg_genai_time"] = self.stats["total_genai_time"] / self.stats["events_processed"]
            if model_used:
                self.stats["model_counts"][model_used] = self.stats["model_counts"].get(model_used, 0) + 1
            log.info(
                "Updated event %s (%s/%s) — %.1fs total",
                event_id, camera, label,
                time.monotonic() - start_ts,
            )
        else:
            log.error("Failed to update event %s", event_id)


# ── MQTT ────────────────────────────────────────────────────────────

def build_mqtt_client(processor: EventProcessor, event_queue: asyncio.Queue, loop: asyncio.AbstractEventLoop) -> mqtt.Client:
    """Create and configure the MQTT client with event callback."""

    def on_connect(client, userdata, flags, reason_code, properties=None):
        if reason_code == 0:
            log.info("MQTT connected, subscribing to frigate/events")
            processor.stats["mqtt_connected"] = True
            client.subscribe("frigate/events")
        else:
            log.error("MQTT connection failed: rc=%d", reason_code)
    def on_disconnect(client, userdata, flags, reason_code, properties=None):
        processor.stats["mqtt_connected"] = False
        if reason_code != 0:
            log.warning("MQTT disconnected: rc=%d", reason_code)

    def on_message(client, userdata, msg):
        log.debug("MQTT message on %s: %s", msg.topic, msg.payload[:200])
        try:
            payload = json.loads(msg.payload.decode())
            event_type = payload.get("type", "")
            after = payload.get("after", {})
            if not after.get("id"):
                return
            eid, camera, label = after["id"], after.get("camera"), after.get("label")

            if event_type == "end" and after.get("has_clip"):
                log.info("End event: %s (%s/%s)", eid, camera, label)
                asyncio.run_coroutine_threadsafe(event_queue.put(after), loop)

            elif event_type == "update":
                before = payload.get("before", {})
                def _desc(e):
                    return ((e.get("data") or {}).get("description") or e.get("description") or "").strip().lower()
                if _desc(after) == "redo" and _desc(before) != "redo":
                    after["has_clip"] = True
                    log.info("Redo trigger: %s (%s/%s)", eid, camera, label)
                    asyncio.run_coroutine_threadsafe(event_queue.put(after), loop)
        except json.JSONDecodeError:
            log.debug("Non-JSON MQTT message on %s", msg.topic)
        except Exception:
            log.exception("Error in MQTT message handler")

    mqtt_host = os.environ.get("MQTT_HOST", "localhost")
    mqtt_port = int(os.environ.get("MQTT_PORT", "1883"))
    mqtt_user = os.environ.get("MQTT_USER", "")
    mqtt_pass = os.environ.get("MQTT_PASSWORD", "")

    client = mqtt.Client(mqtt.CallbackAPIVersion.VERSION2)
    if mqtt_user:
        client.username_pw_set(mqtt_user, mqtt_pass)
    client.on_connect = on_connect
    client.on_message = on_message
    client.on_disconnect = on_disconnect

    # Retry connection
    while True:
        try:
            client.connect(mqtt_host, mqtt_port, 60)
            break
        except Exception:
            log.warning("MQTT connection failed, retrying in 5s...")
            time.sleep(5)

    client.loop_start()
    return client


# ── main ────────────────────────────────────────────────────────────

async def async_main(prompts_path: str, provider_path: str) -> None:
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(name)s] %(levelname)s: %(message)s",
    )

    prompts = load_json(prompts_path)
    provider_cfg = load_json(provider_path)

    log.info("GenAI provider: %s / %s", provider_cfg.get("provider"), provider_cfg.get("model"))
    log.info("Describe labels: %s", ", ".join(sorted(prompts.get("describe", []))))

    processor = EventProcessor(prompts, provider_cfg)
    event_queue: asyncio.Queue = asyncio.Queue(maxsize=100)

    # Start MQTT in background thread
    client = build_mqtt_client(processor, event_queue, asyncio.get_running_loop())

    log.info("Frigate GenAI sidecar running, waiting for events...")
    # Start HTTP server for manual reprocess triggers
    import threading
    from http.server import HTTPServer, BaseHTTPRequestHandler

    class ReprocessHandler(BaseHTTPRequestHandler):
        def log_message(self, fmt, *args):
            log.debug("HTTP: %s", fmt % args)

        def _cors(self):
            self.send_header("Access-Control-Allow-Origin", "*")
            self.send_header("Access-Control-Allow-Methods", "GET, POST, OPTIONS")
            self.send_header("Access-Control-Allow-Headers", "Content-Type")

        def do_OPTIONS(self):
            self.send_response(200)
            self._cors()
            self.end_headers()

        def do_GET(self):
            if self.path == "/":
                self.send_response(200)
                self._cors()
                self.send_header("Content-type", "text/html; charset=utf-8")
                self.end_headers()
                self.wfile.write(UI_HTML.encode())
            elif self.path == "/api/stats":
                self.send_response(200)
                self._cors()
                self.send_header("Content-type", "application/json; charset=utf-8")
                self.end_headers()
                self.wfile.write(json.dumps(processor.stats).encode())
            elif self.path.startswith("/api/events/") and self.path.endswith("/snapshot.jpg"):
                self._proxy("image/jpeg")
            elif self.path.startswith("/api/events"):
                self._proxy("application/json")
            else:
                self.send_response(404)
                self.end_headers()

        def _proxy(self, content_type):
            base_url = os.environ.get("FRIGATE_BASE_URL", "http://localhost:5000")
            try:
                resp = urllib.request.urlopen(base_url + self.path, timeout=15)
                self.send_response(resp.status)
                self._cors()
                self.send_header("Content-type", content_type)
                self.end_headers()
                self.wfile.write(resp.read())
            except Exception as e:
                self.send_response(502)
                self._cors()
                self.end_headers()
                self.wfile.write(str(e).encode())

        def do_POST(self):
            if self.path.startswith("/reprocess/"):
                event_id = self.path.split("/reprocess/", 1)[1]
                base_url = os.environ.get("FRIGATE_BASE_URL", "http://localhost:5000")
                try:
                    resp = urllib.request.urlopen(f"{base_url}/api/events/{event_id}", timeout=10)
                    event = json.loads(resp.read())
                    mqtt_msg = json.dumps({
                        "type": "end",
                        "after": {
                            "id": event["id"],
                            "camera": event["camera"],
                            "label": event["label"],
                            "start_time": event["start_time"],
                            "end_time": event["end_time"],
                            "has_clip": event.get("has_clip", True),
                            "has_snapshot": event.get("has_snapshot", True),
                        }
                    })
                    client.publish("frigate/events", mqtt_msg)
                    self.send_response(200)
                    self._cors()
                    self.send_header("Content-type", "text/plain")
                    self.end_headers()
                    msg = f"Reprocessing {event_id} ({event['camera']}/{event['label']})"
                    self.wfile.write(msg.encode())
                    log.info("HTTP reprocess: %s", msg)
                except Exception as e:
                    self.send_response(500)
                    self._cors()
                    self.end_headers()
                    self.wfile.write(str(e).encode())
            else:
                self.send_response(404)
                self.end_headers()

    http_port = int(os.environ.get("HTTP_PORT", "9090"))
    http_host = os.environ.get("HTTP_HOST", "127.0.0.1")
    httpd = HTTPServer((http_host, http_port), ReprocessHandler)
    thread = threading.Thread(target=httpd.serve_forever, daemon=True)
    thread.start()
    log.info("HTTP reprocess endpoint on http://%s:%d", http_host, http_port)


    try:
        while True:
            event = await event_queue.get()
            try:
                await processor.process(event)
            except Exception:
                log.exception("Error processing event %s", event.get("id"))
    finally:
        client.loop_stop()
        client.disconnect()


