# Maps tool_name → activity function.
# Use _get_tool_queue() for queue-routed dispatch — pass the genai_queue
# (GEMINI_TASK_QUEUE or OLLAMA_TASK_QUEUE) so tools follow the model.
_TOOL_ACTIVITIES: dict[str, object] = {
    "get_snapshot":    tool_get_snapshot_activity,
    "show_frame":      tool_show_frame_activity,
    "transcode":       tool_transcode_activity,
    "crop":            tool_crop_activity,
    "compact":         tool_compact_activity,
    "set_description": tool_set_description_activity,
    "upscale":         tool_upscale_activity,
}

def _get_tool_queue(tool_name: str, genai_queue: str) -> tuple[object, str]:
    """Return (activity_fn, task_queue) for a tool call.
    Transcode always routes to ffmpeg; everything else follows the genai model."""
    activity_fn = _TOOL_ACTIVITIES[tool_name]
    if tool_name == "transcode":
        return activity_fn, FFMPEG_TASK_QUEUE
    if tool_name == "upscale":
        return activity_fn, TASK_QUEUE
    return activity_fn, genai_queue

@workflow.defn
class GenAIWorkflow:
    """Orchestrates GenAI event processing: extract, genai, describe."""

    @workflow.run
    async def run(self, input_data: dict) -> None:

        try:
            event_id = input_data["event_id"]
            camera = input_data["camera"]
            label = input_data["label"]
            start_time = input_data["start_time"]
            end_time = input_data["end_time"]

            log_ctx = f"event={event_id} camera={camera} label={label}"

            workflow.logger.info("GenAI workflow started: %s", log_ctx)

            # Step 0: select model (car override is in input_data already)
            model = input_data.get("model")
            if model is None:
                model = await workflow.execute_activity(
                    select_model_activity,
                    input_data,
                    start_to_close_timeout=timedelta(seconds=10),
                    retry_policy=_ACTIVITY_RETRY,
                )
                input_data["model"] = model
            # Publish model as search attribute for filtering
            workflow.upsert_search_attributes([
                SearchAttributePair(_SEARCH_MODEL, model),
            ])
            workflow.logger.info("model=%s", model)

            skip_frames = input_data.get("skip_frames", False)

            # Step 1: fetch snapshot + extract ffmpeg frames (parallel)
            if skip_frames:
                frames_dir = await workflow.execute_activity(
                    fetch_snapshot_activity,
                    input_data,
                    start_to_close_timeout=timedelta(seconds=30),
                    retry_policy=_ACTIVITY_RETRY,
                )
            else:
                snapshot_dir, (ffmpeg_dir, frame_count) = await asyncio.gather(
                    workflow.execute_activity(
                        fetch_snapshot_activity,
                        input_data,
                        start_to_close_timeout=timedelta(seconds=30),
                        retry_policy=_ACTIVITY_RETRY,
                    ),
                    workflow.execute_activity(
                        transcode_into_parts_activity,
                        input_data,
                        task_queue=FFMPEG_TASK_QUEUE,
                        start_to_close_timeout=timedelta(hours=1),
                        heartbeat_timeout=timedelta(seconds=10),
                        retry_policy=_EXTRACT_RETRY,
                    ),
                )
                frames_dir = snapshot_dir

                # Upsert duration + frame count as search attributes
                dur_sec = int(end_time - start_time)
                workflow.upsert_search_attributes([
                    SearchAttributePair(_SEARCH_DURATION, dur_sec),
                ])
                workflow.logger.info("duration=%ds frames=%d", dur_sec, frame_count)


            # Step 2: progress patch
            await workflow.execute_activity(
                update_description_activity,
                args=[event_id, f"Processing {label} on {camera}..."],
                start_to_close_timeout=timedelta(seconds=15),
                retry_policy=_ACTIVITY_RETRY,
            )

            # Step 3: initialize agent state on disk (activity — sandboxed in workflow)
            genai_queue = GEMINI_TASK_QUEUE if model.startswith("gemini/") else OLLAMA_TASK_QUEUE
            init_result = await workflow.execute_activity(
                init_agent_state_activity,
                arg={
                    "event_id": event_id, "camera": camera, "label": label,
                    "frames_dir": frames_dir, "prompts_path": input_data["prompts_path"],
                    "data_box": input_data.get("data_box"),
                    "start_time": start_time, "end_time": end_time,
                },
                task_queue=TASK_QUEUE,
                start_to_close_timeout=timedelta(seconds=10),
                retry_policy=_ACTIVITY_RETRY,
            )
            msg_path = init_result["msg_path"]
            max_frames = init_result["max_frames"]

            turn_arg = {
                "msg_path": msg_path,
                "provider_path": input_data["provider_path"],
                "model": model,
                "event_id": event_id,
                "camera": camera,
                "label": label,
            }

            # Accumulators for stats
            total_cost = {"prompt": 0, "completion": 0, "cached": 0}
            turns_low = 0
            turns_high = 0
            turns_max = 0
            turns_transcode = 0
            trace_entries: list[dict] = []
            description = None
            confidence = None

            for turn in range(MAX_TURNS):
                turn_arg["turn_num"] = turn + 1
                turn_arg["max_turns"] = MAX_TURNS
                result = await workflow.execute_activity(
                    run_genai_turn_activity,
                    arg=turn_arg,
                    task_queue=genai_queue,
                    start_to_close_timeout=timedelta(seconds=300),
                    heartbeat_timeout=timedelta(seconds=15),
                    retry_policy=_GENAI_RETRY,
                )

                pt = result.get("prompt_tokens", 0)
                ct = result.get("completion_tokens", 0)
                cached = result.get("cached_tokens", 0)
                total_cost["prompt"] += pt
                total_cost["completion"] += ct
                total_cost["cached"] += cached
                trace_entries.append({
                    "type": "turn", "turn": turn + 1,
                    "prompt_tokens": pt, "completion_tokens": ct,
                    "cached": cached > 0,
                })
                workflow.set_current_details(
                    f"Turn {turn+1}/{MAX_TURNS} | "
                    f"tokens: {total_cost['prompt']}+{total_cost['completion']} "
                    f"(cached: {total_cost['cached']}) | "
                    f"tools: {len(trace_entries)}"
                )

                if result.get("description"):
                    description = result["description"]
                    confidence = result.get("confidence", "medium")
                    trace_entries.append({
                        "type": "tool_call", "name": "set_description",
                        "confidence": confidence,
                        "description": description[:200],
                    })
                    break

                if result.get("text_only"):
                    # Text-only response — nudge
                    trace_entries.append({"type": "nudge", "reason": "no_tool_call"})
                    continue




                # ── Parallel tool dispatch ──────────────────────────
                # Phase 1: Launch all tool activities in parallel
                handles: list[tuple[dict, dict, object]] = []
                for tc in result.get("tool_calls", []):
                    te = {"type": "tool_call", "name": tc["name"], "args": tc.get("args", {})}
                    if tc["name"] not in _TOOL_ACTIVITIES:
                        workflow.logger.warning("Unknown tool: %s", tc["name"])
                        te["error"] = f"Unknown tool: {tc['name']}"
                        trace_entries.append(te)
                        continue

                    activity_fn, task_queue = _get_tool_queue(tc["name"], genai_queue)
                    retry = _ACTIVITY_RETRY if tc["name"] in ("transcode", "upscale") else None
                    if tc["name"] == "transcode":
                        timeout = timedelta(seconds=60)
                    elif tc["name"] == "upscale":
                        timeout = timedelta(seconds=180)
                    else:
                        timeout = timedelta(seconds=30)

                    handle = workflow.start_activity(
                        activity_fn,
                        arg={
                            "msg_path": msg_path,
                            "args": tc.get("args", {}),
                            "event_id": event_id,
                            "max_frames": max_frames,
                            "camera": camera,
                            "start_time": start_time,
                            "end_time": end_time,
                            "provider_path": input_data["provider_path"],
                            "model": model,
                        },
                        task_queue=task_queue,
                        start_to_close_timeout=timeout,
                        retry_policy=retry,
                    )
                    handles.append((tc, te, handle))

                # Phase 2: Await all, collect stats + outcomes
                if handles:
                    outcomes: list[dict] = []
                    for tc, te, handle in handles:
                        outcome = await handle
                        if tc["name"] == "transcode" and isinstance(outcome, dict):
                            n = outcome.get("frames_extracted", 0)
                            turns_transcode += n
                        if isinstance(outcome, dict):
                            te.update(outcome)
                        if tc["name"] == "show_frame" and isinstance(outcome, dict):
                            res = outcome.get("resolution")
                            n_frames = outcome.get("frames_shown", 0)
                            if res in ("tiny", "low"):
                                turns_low += n_frames
                            elif res == "med":
                                turns_high += n_frames
                            elif res in ("high", "max"):
                                turns_max += n_frames
                        trace_entries.append(te)
                        outcomes.append(outcome)

                    # Apply all messages to state in one batch
                    await workflow.execute_activity(
                        apply_tool_messages_activity,
                        arg={"msg_path": msg_path, "outcomes": outcomes},
                        task_queue=genai_queue,
                        start_to_close_timeout=timedelta(seconds=10),
                        retry_policy=_ACTIVITY_RETRY,
                    )

                workflow.set_current_details(
                    f"Turn {turn+1}/{MAX_TURNS} | "
                    f"tokens: {total_cost['prompt']}+{total_cost['completion']} "
                    f"(cached: {total_cost['cached']}) | "
                    f"tools: {len(trace_entries)}"
                )

            if not description:
                description = f"Agentic failed: max turns ({MAX_TURNS}) exceeded without set_description"
                confidence = "low"
                workflow.logger.warning("Agent: max turns exceeded for %s", event_id)

            # Format trace for UI + memo
            def _format_trace(entries: list[dict]) -> str:
                lines = []
                for e in entries:
                    if e["type"] == "turn":
                        prefix = "(cached) " if e.get("cached") else ""
                        lines.append(
                            f"Turn {e['turn']}: {e['prompt_tokens']}+{e['completion_tokens']} tokens {prefix}"
                        )
                    elif e["type"] == "tool_call":
                        n = e["name"]
                        a = e.get("args", {})
                        if n == "get_snapshot":
                            lines.append("  -> get_snapshot()")
                        elif n == "show_frame":
                            lines.append(
                                f"  -> show_frame({a.get('source', '?')[:50]}, {a.get('resolution', '?')}): {e.get('frames_shown', '?')} frames"
                            )
                        elif n == "crop":
                            lines.append(
                                f"  -> crop({a.get('source', '')[:30]}, count={e.get('count', '?')})"
                            )
                        elif n == "transcode":
                            lines.append(
                                f"  -> transcode(start={a.get('start')}, dur={a.get('duration', 1)}, {e.get('fps', '?')}fps): {e.get('frames_extracted', '?')} frames"
                            )
                        elif n == "compact":
                            lines.append("  -> compact()")
                        elif n == "set_description":
                            lines.append(
                                f"  -> set_description(confidence={e.get('confidence')}): {e.get('description', '')[:120]}"
                            )
                        else:
                            lines.append(f"  -> {n}()")
                        if e.get("error"):
                            lines.append(f"    ERROR: {e['error']}")
                    elif e["type"] == "nudge":
                        lines.append(f"  [nudge] {e['reason']}")
                return "\n".join(lines)

            trace_text = _format_trace(trace_entries)
            trace_text += (
                f"\n\nTotal: {turn+1} turns, {turns_low} L/{turns_high} H/{turns_max} M/{turns_transcode} T, "
                f"{total_cost['prompt']}+{total_cost['completion']} tokens"
            )

            workflow.set_current_details(trace_text)
            workflow.upsert_memo({
                "AgentSummary": trace_text,
                "AgentTokens": (
                    f"in={total_cost['prompt']} "
                    f"out={total_cost['completion']} "
                    f"cached={total_cost.get('cached', 0)}"
                ),
                "AgentTurns": (
                    f"turns={turn+1} "
                    f"low={turns_low} "
                    f"high={turns_high} "
                    f"max={turns_max} "
                    f"transcode={turns_transcode}"
                ),
                "AgentConfidence": confidence or "N/A",
            })
            if confidence:
                workflow.upsert_search_attributes([
                    SearchAttributePair(_SEARCH_CONFIDENCE, confidence),
                ])
            workflow.upsert_search_attributes([
                SearchAttributePair(_SEARCH_TRANSCODE, turns_transcode > 0),
            ])

            # Persist agent logs (fire-and-forget activity)
            trace_header = f"Event: {event_id}  Camera: {camera}  Label: {label}  Model: {model}\n"
            trace_header += f"Frames available: {max_frames}  Snapshot: yes\n"
            trace_header += f"Confidence: {confidence or 'N/A'}\n\n"
            await workflow.execute_activity(
                save_agent_log_activity,
                arg={"event_id": event_id, "trace_text": trace_header + trace_text},
                task_queue=genai_queue,
                start_to_close_timeout=timedelta(seconds=10),
            )

            stats = {
                "event_id": event_id, "camera": camera, "label": label, "model": model,
                "provider_path": input_data["provider_path"],
                "turns": turn + 1, "turns_low": turns_low, "turns_high": turns_high,
                "turns_max": turns_max, "turns_transcode": turns_transcode,
                "total_cost": total_cost, "confidence": confidence,
                "trace": trace_header + trace_text,
            }
            summary = await workflow.execute_activity(
                summarize_agent_activity,
                args=[stats],
                task_queue=genai_queue,
                start_to_close_timeout=timedelta(seconds=120),
                retry_policy=_ACTIVITY_RETRY,
            )
            if summary:
                description = f"{description}\n\n[{summary}]"

            # Step 4: final result
            if not description:
                workflow.logger.error("No description from GenAI: %s", log_ctx)
                await workflow.execute_activity(
                    update_description_activity,
                    args=[event_id, "Failed: GenAI returned no description"],
                    start_to_close_timeout=timedelta(seconds=15),
                    retry_policy=_ACTIVITY_RETRY,
                )
                return

            await workflow.execute_activity(
                update_description_activity,
                args=[event_id, description],
                start_to_close_timeout=timedelta(seconds=15),
                retry_policy=_ACTIVITY_RETRY,
            )

            workflow.logger.info("GenAI workflow completed: %s", log_ctx)
            await workflow.execute_activity(
                cleanup_cancelled_activity,
                args=[input_data, False],
                start_to_close_timeout=timedelta(seconds=30),
            )
        except asyncio.CancelledError:
            workflow.logger.info("GenAI workflow cancelled, cleaning up: %s", log_ctx)
            await workflow.execute_activity(
                cleanup_cancelled_activity,
                args=[input_data, True],
                start_to_close_timeout=timedelta(seconds=30),
            )
            raise
