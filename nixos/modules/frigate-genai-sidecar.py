    ollama_task = asyncio.create_task(ollama_worker.run())
    log.info("Temporal workers started: %s + %s + %s + %s",
             TASK_QUEUE, FFMPEG_TASK_QUEUE, GEMINI_TASK_QUEUE, OLLAMA_TASK_QUEUE)

